package com.example.nutritiondatabaseapp;

public class User {
    private int height;
    private int weight;

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }


    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }
}
